import { readdir, writeFile, mkdir } from "node:fs/promises";
import { readFileSync } from "node:fs";
import { join, resolve, relative, dirname } from "node:path";
import esbuild from "esbuild";

function isSourceFile(file, exts, dtsPath) {
	const clean = resolve(file.split("?")[0]);
	if (clean === resolve(dtsPath)) return false;
	return !clean.endsWith(".d.ts") && exts.some(e => clean.endsWith(e));
}

async function walk(dir, exts, dtsPath) {
	try {
		const dns = await readdir(dir, { withFileTypes: true });
		const f = await Promise.all(dns.map(d => {
			if (d.name === "node_modules" || d.name.startsWith(".")) return [];
			const p = join(dir, d.name);
			return d.isDirectory() ? walk(p, exts, dtsPath) : resolve(p);
		}));
		return f.flat().filter(file => isSourceFile(file, exts, dtsPath));
	} catch {
		return [];
	}
}

async function writeDeclarationFile(map, dtsPath) {
	const dtsDir = dirname(dtsPath);
	let content = "// This file was generated by auto-imports\ndeclare global {";
	for (const name in map) {
		const { file, isDefault } = map[name];
		const rel = relative(dtsDir, file).replace(/\\/g, "/").replace(/\.[jt]sx?$/, "");
		const path = rel.startsWith(".") ? rel : `./${rel}`;
		content += `\n	var ${name}: typeof import("${path}").${isDefault ? "default" : name};`;
	}
	content += "\n}\n\nexport {};";
	try {
		await mkdir(dtsDir, { recursive: true });
		await writeFile(dtsPath, content);
	} catch (e) {
		console.error(`Failed dts: ${e.message}`);
	}
}

function findUppercaseWords(code) {
	const clean = code.replace(/\/\*[\s\S]*?\*\/|\/\/.*/g, "");
	const matches = clean.match(/\b[A-Z][\w$]*\b/g);
	return [...new Set(matches || [])];
}

export default function autoImports(options = {}) {
	const opts = {
		src: "src",
		extensions: [".js", ".jsx", ".ts", ".tsx"],
		dts: "src/global.d.ts",
		...options
	};
	let exportMap = {};

	const processFiles = async (files, map, server) => {
		// 1. Clear existing map entries for these files
		if (!files.length) return;
		for (const name in map) {
			if (files.includes(map[name].file)) delete map[name];
		}
		// 2. Build with esbuild and parse exports
		try {
			const result = await esbuild.build({
				entryPoints: files,
				bundle: false,
				metafile: true,
				write: false,
				outdir: "dist",
				format: "esm",
				logLevel: "silent"
			});
			for (const outputData of Object.values(result.metafile.outputs)) {
				if (!outputData.entryPoint) continue;
				const fileAbs = resolve(process.cwd(), outputData.entryPoint);
				for (let name of outputData.exports || []) {
					let isDefault = false;
					if (name === "default") {
						const code = readFileSync(fileAbs, "utf8");
						const rx = /export\s+default\s+(?:async\s+)?(?:function\s+|class\s+)?\s*([\w$]+)/;
						const m = code.match(rx);
						if (!m || ["function", "class", "async"].includes(m[1])) continue;
						name = m[1];
						isDefault = true;
					}
					// 3. Check for duplicate identifier conflicts
					if (map[name] && map[name].file !== fileAbs) {
						const msg = `Conflict for identifier "${name}"`;
						const details = `Paths:\n1. ${fileAbs}\n2. ${map[name].file}`;
						console.error(`[auto-imports] ${msg}\n${details}`);
						if (server) server.ws.send({
							type: "error",
							err: {
								message: msg,
								stack: details,
								plugin: "auto-imports",
								id: fileAbs
							}
						});
						continue;
					}
					map[name] = { file: fileAbs, isDefault };
				}
			}
		} catch (e) {
			// Suppress general build errors to only log conflicts
		}
	};

	const refreshMap = async (targetFiles, server) => {
		const dtsPath = resolve(process.cwd(), opts.dts);
		const newMap = targetFiles ? { ...exportMap } : {};
		if (targetFiles) {
			const validFiles = targetFiles.filter(f => isSourceFile(f, opts.extensions, dtsPath));
			if (!validFiles.length) return;
			await processFiles(validFiles, newMap, server);
		} else {
			const allFiles = await walk(resolve(process.cwd(), opts.src), opts.extensions, dtsPath);
			await processFiles(allFiles, newMap, server);
		}
		exportMap = newMap;
		await writeDeclarationFile(exportMap, dtsPath);
		if (server) server.moduleGraph.invalidateAll();
	};

	return {
		name: "auto-imports",
		async buildStart() {
			await refreshMap();
		},
		configureServer(server) {
			server.watcher.on("add", p => refreshMap([resolve(p)], server));
			server.watcher.on("change", p => refreshMap([resolve(p)], server));
			server.watcher.on("unlink", () => refreshMap(null, server));
		},
		async transform(code, id) {
			const dtsPath = resolve(process.cwd(), opts.dts);
			const cleanId = id.split("?")[0];
			if (cleanId.includes("node_modules")) return null;
			if (!isSourceFile(cleanId, opts.extensions, dtsPath)) return null;
			const words = findUppercaseWords(code);
			let injected = "";
			for (const name of words) {
				if (!exportMap[name] || exportMap[name].file === cleanId) continue;
				const isImported = new RegExp(`import\\s+[^\\n'"]*?\\b${name}\\b`).test(code);
				const isDeclared = new RegExp(
					`(?:const|let|var|function|class)[\\s\\{,]*\\b${name}\\b`
				).test(code);
				if (isImported || isDeclared) continue;
				const { file, isDefault } = exportMap[name];
				const rel = relative(dirname(cleanId), file).replace(/\\/g, "/").replace(/\.[jt]sx?$/, "");
				const path = rel.startsWith(".") ? rel : `./${rel}`;
				if (isDefault) injected += `import ${name} from "${path}";\n`;
				else injected += `import { ${name} } from "${path}";\n`;
			}
			if (!injected) return null;
			return { code: injected + code, map: null };
		}
	};
}