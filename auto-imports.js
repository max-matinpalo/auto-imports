import { readdir, writeFile, mkdir } from "node:fs/promises";
import { readFileSync } from "node:fs";
import { join, resolve, relative, dirname } from "node:path";
import esbuild from "esbuild";

function isSourceFile(file, exts, dtsPath) {
	const clean = resolve(file.split("?")[0]);
	if (clean === resolve(dtsPath)) return false;
	return !clean.endsWith(".d.ts") && exts.some(e => clean.endsWith(e));
}

async function walk(dir, exts, dtsPath) {
	try {
		const dns = await readdir(dir, { withFileTypes: true });
		const files = await Promise.all(dns.map(d => {
			if (d.name === "node_modules" || d.name.startsWith(".")) return [];
			const p = join(dir, d.name);
			return d.isDirectory() ? walk(p, exts, dtsPath) : resolve(p);
		}));
		return files.flat().filter(file => isSourceFile(file, exts, dtsPath));
	} catch {
		return [];
	}
}

async function writeDeclarationFile(map, dtsPath) {
	const dtsDir = dirname(dtsPath);
	let content = "// This file was generated by auto-imports\ndeclare global {";
	for (const [name, { file, isDefault, isLib }] of Object.entries(map)) {
		let path = file;
		if (!isLib) {
			const rel = relative(dtsDir, file).replace(/\\/g, "/").replace(/\.[jt]sx?$/, "");
			path = rel.startsWith(".") ? rel : `./${rel}`;
		}
		content += `\n\tvar ${name}: typeof import("${path}").${isDefault ? "default" : name};`;
	}
	content += "\n}\n\nexport {};";
	try {
		await mkdir(dtsDir, { recursive: true });
		await writeFile(dtsPath, content);
	} catch (e) {
		console.error(`Failed dts: ${e.message}`);
	}
}

function findIdentifiers(code) {
	const clean = code.replace(/\/\*[\s\S]*?\*\/|\/\/.*$/gm, "");
	return [...new Set(clean.match(/\b[a-zA-Z_$][\w$]*\b/g) || [])];
}

export default function autoImports(options = {}) {
	const rawLibs = options.libs || (options.lib ? [options.lib] : []);
	const opts = {
		src: "src",
		extensions: [".js", ".jsx", ".ts", ".tsx"],
		dts: "src/global.d.ts",
		libs: Array.isArray(rawLibs) ? rawLibs : [rawLibs],
		...options
	};

	let exportMap = Object.create(null);
	let fileSignatures = Object.create(null);
	let queue = Promise.resolve();
	let viteServer = null;

	const addExport = (name, exp) => {
		const old = exportMap[name];
		if (old && old.file !== exp.file) {
			const msg = `Conflict for identifier "${name}"`;
			const details = `Paths:\n1. ${exp.file}\n2. ${old.file}`;
			console.error(`[auto-imports] ${msg}\n${details}`);
			if (viteServer) viteServer.ws.send({
				type: "error",
				err: { message: msg, stack: details, plugin: "auto-imports", id: exp.file }
			});
			return;
		}
		exportMap[name] = exp;
	};

	const parseFileExports = async (file) => {
		const exports = [];
		try {
			const result = await esbuild.build({
				entryPoints: [file],
				bundle: false,
				metafile: true,
				write: false,
				outdir: "dist",
				format: "esm",
				logLevel: "silent",
				loader: { ".js": "jsx", ".jsx": "jsx", ".ts": "ts", ".tsx": "tsx" }
			});
			for (const outputData of Object.values(result.metafile.outputs)) {
				if (!outputData.entryPoint) continue;
				const fileAbs = resolve(process.cwd(), outputData.entryPoint);
				for (let name of outputData.exports || []) {
					let isDefault = false;
					if (name === "default") {
						const code = readFileSync(fileAbs, "utf8");
						const rx = /export\s+default\s+(?:async\s+)?(?:function\s+|class\s+)?\s*([\w$]+)/;
						const m = code.match(rx);
						if (!m || ["function", "class", "async"].includes(m[1])) continue;
						name = m[1];
						isDefault = true;
					}
					exports.push({ name, file: fileAbs, isDefault });
				}
			}
		} catch (e) {
			console.error(`[auto-imports] Failed to parse ${file}`);
			for (const err of e.errors || []) console.error(err.text);
			if (!e.errors) console.error(e.message);
		}
		return exports;
	};

	const runSync = (task) => {
		queue = queue.then(async () => {
			try {
				await task(resolve(process.cwd(), opts.dts));
			} catch (e) {
				console.error(`[auto-imports] Task failed: ${e.message}`);
			}
		});
		return queue;
	};

	const rebuildMap = async (dtsPath) => {
		exportMap = Object.create(null);
		fileSignatures = Object.create(null);
		for (const lib of opts.libs) {
			try {
				const mod = await import(lib);
				for (const key of Object.keys(mod)) {
					if (key !== "default")
						addExport(key, { file: lib, isDefault: false, isLib: true });
				}
			} catch { }
		}
		const allFiles = await walk(resolve(process.cwd(), opts.src), opts.extensions, dtsPath);
		for (const file of allFiles) {
			const discovered = await parseFileExports(file);
			fileSignatures[file] = discovered.map(e => `${e.name}:${e.isDefault}`).sort().join(",");
			for (const exp of discovered) addExport(exp.name, exp);
		}
		await writeDeclarationFile(exportMap, dtsPath);
	};

	return {
		name: "auto-imports",

		async buildStart() {
			await runSync(rebuildMap);
		},

		configureServer(server) {
			viteServer = server;
			const handleStructuralUpdate = () => {
				runSync(async (dtsPath) => {
					await rebuildMap(dtsPath);
					server.moduleGraph.invalidateAll();
				});
			};

			server.watcher.on("add", handleStructuralUpdate);
			server.watcher.on("unlink", handleStructuralUpdate);
			server.watcher.on("change", p => {
				const file = resolve(p);
				runSync(async (dtsPath) => {
					if (!isSourceFile(file, opts.extensions, dtsPath)) return;
					const newExports = await parseFileExports(file);
					const newSig = newExports.map(e => `${e.name}:${e.isDefault}`).sort().join(",");
					if (fileSignatures[file] === newSig) return;

					await rebuildMap(dtsPath);
					server.moduleGraph.invalidateAll();
				});
			});
		},

		async transform(code, id) {
			if (id.startsWith("virtual:") || id.startsWith("\0")) return null;
			const dtsPath = resolve(process.cwd(), opts.dts);
			const cleanId = id.split("?")[0];
			if (cleanId.includes("node_modules") || !isSourceFile(cleanId, opts.extensions, dtsPath))
				return null;

			let injected = "";
			for (const name of findIdentifiers(code)) {
				if (!exportMap[name] || exportMap[name].file === cleanId) continue;
				if (new RegExp(`import\\s+[^\\n'"]*?\\b${name}\\b`).test(code)) continue;
				if (new RegExp(`(?:const|let|var|function|class)[\\s\\{,]*\\b${name}\\b`).test(code))
					continue;

				const { file, isDefault, isLib } = exportMap[name];
				let path = file;
				if (!isLib) {
					const rel = relative(dirname(cleanId), file).replace(/\\/g, "/").replace(/\.[jt]sx?$/, "");
					path = rel.startsWith(".") ? rel : `./${rel}`;
				}
				injected += isDefault ? `import ${name} from "${path}";\n` : `import { ${name} } from "${path}";\n`;
			}
			return injected ? { code: injected + code, map: null } : null;
		}
	};
}